  <div class="card-body">
    <h5 class="card-title">5 Manfaat Air Garam untuk Kaki Biar Selalu Sehat</h5>
    <p class="card-text">Air garam juga bisa dimanfaatkan sebagai air rendaman kaki. Apalagi jika garam yang digunakan sebagai campuran adalah garam Epsom.
      Penggunaannya pun bukan untuk bumbu memasak, melainkan sebagai garam mandi atau berendam dan inilah manfaat air garam untuk kaki biar selalu sehat.</p>
  </div>
  <hr>
  <details>
    <summary>1. Pengangkat Sel-sel Kulit Mati</summary>
    <P>Kaki yang banyak aktivitasnya dekat lantaidan tanah jelas jauh lebih mudah kotor. Salah satu cara menjaga kesehatan dankebersihan kaki adalah dengan mengeksfoliasinya (proses mengangkat sel-selkulit mati). Nah, air garam (tak harus menggunakan garam Epsom jika tidak ada)bisa menjadi air rendaman sekaligus eksfoliator bagi kaki.</P>
    <p>Mineral-mineral dalam garam yang dicampur pada air inilah yang mampu membuat pori-pori terbuka supaya proses pembersihan jadi lebih mudah. Pada pemilik jenis kulit sensitif, melansir dari Style Caster, scrub dari garam cocok untuk kaki dan bagian tubuh lain selain wajah, jadi layak kok dicoba.</P>
  </details>
  <hr>
  <details>
    <summary>2. Pelancar Peredaran Darah</summary>
    <p>Saat menggunakan air garam sebagai eksfoliator, otomatis kita akan menggosok-gosok kaki agar sel-sel kulit mati mudah terangkat. Di kala melakukan ini, sebenarnya kita juga sedang memberikan pijatan pada kaki agar peredaran darah lebih lancar lho. Jadi selain mampu membersihkan kulit kaki, air garam berguna memperlancar aliran darah sehingga memaksimalkan berbagai skincare yang dipakai agar terserap sempurna ke dalam kulit.</p>
  </details>
  <hr>
    <details>
      <summary>3. Mengatasi Inflamasi dan Nyeri</summary>
    <p>Jika menggunakan garam Epsom sebagai campuran air untuk rendaman kaki, kombinasi sulfat dan magnesiumnya tak hanya membersihkan kaki dari sel-sel kulit mati, tapi juga siap mengurangi radang dan meredakan nyeri sendi serta kram pada otot yang sering muncul. Jadi ketika kaki sering nyeri karena radang sendi dan otot, berendamlah dengan air garam hangat yang akan menghilangkan rasa sakit secara alami.</p>
  </details>
  <hr>
    <details>
      <summary>4. Melemaskan Otot dan Saraf</summary>
    <p>Gunakan air garam untuk merendam kaki karena otomatis kaki akan jadi lebih rileks. Otot serta saraf pasti akan melemas dan fungsinya akan kembali baik.</p>
  </details>
  <hr>
    <details>
      <summary>5. Menghilangkan Bau Kaki</summary>
    <p>Air garam (khususnya jika pakai garam Epsom)biasanya juga efektif dalam menghilangkan bau kaki. Berbagai kuman pemicu baukaki akan dibasmi dengan garam ini, termasuk juga jamur pada kuku. Bagi pemilikmasalah kuku tumbuh ke dalam, air garam ini pun dapat menjadi penolong.</p>
  </details>
