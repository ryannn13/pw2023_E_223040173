<h2 Style="margin-left: 1vh;">Tips Kesehatan</h2>
<div class="card" style="width: 20rem; float:left; margin:1.2rem; margin-top: -0.3vh;">
  <img src="image/isi/sample.png" class="card-img-top" alt="...">
  <div class="card-body">
    <h5 class="card-title">4 Sehat 5 Sempurna</h5>
    <p class="card-text">Makanan 4 sehat 5 sempurna adalah menu makanan yang lengkap dan mengandung zat gizi yang dibutuhkan oleh tubuh seperti karbohidrat, protein, vitamin dan mineral.</p>
  </div>
  <hr>
  <details>
    <summary>1.Makanan Pokok</summary>
    <P>Nasi, Jagung, Sagu, Singkong, kentang, atau umbi-umbian, Gandum, Nasi adalah sumber energi bagi tubuh anak. Meskipun baik, namun mengonsumsi nasi sebaiknya dibatasi atau bisa diganti dengan nasi merah.</P>
  </details>
  <hr>
  <details>
    <summary>2. Lauk pauk</summary>
    <p>Beberapa contoh lauk pauk yang wajib untuk dikonsumsi: Daging ayam, sapi, kambing, ikan, dan bebek. Salah satu contoh lauk pauk yang banyak dikonsumsi masyarakat Indonesia adalah ikan. 
      Selain lezat, ikan juga banyak mengandung banyak protein dan omega 3 yang bagus bagi perkembangan otak. Beberapa jenis ikan yang baik dikonsumsi adalah salmon dan tuna. Tak hanya ikan, ayam juga merupakan jenis lauk yang paling banyak dikonsumsi masyarakat Indonesia. Ayam mengandung protein hewani dan juga lemak. 
      Namun, tidak disarankan untuk mengonsumsi daging ayam setiap hari. Pasalnya bagian hati dan usus ayam banyak mengandung lemak.</p>
  </details>
  <hr>
    <details>
      <summary>3. Sayur-sayuran</summary>
    <p>Sayur-sayuran merupakan makanan sehat yang baik untuk mencegah berbagai penyakit. Beberapa jenis sayur-sayuran yang baik dikonsumsi antara lain: 
      <ol>
        <li>Sawi mengandung zat hijau karena dapat menyehatkan tulang, mencegah kanker, baik untuk diabetes, menyehatkan kulit dan rambut, serta membantu tidur dan suasana hati. Sayuran sawi memiliki vitamin K yang mengandung folat, thiamin, niacin fosfor dan kalium.</li>
        <li>Kubis, brokoli, kembang kol, Ketiga jenis sayuran tersebut memiliki kandungan tinggi indoles, kandungan ini memiliki perlindungan anti kanker seperti kanker usus dan payudara.</li>
        <li>Sayuran brokoli dan kembang kol juga memiliki flavonoid yang berfungsi meningkatkan kekebalan tubuh</li>
      </ol>
    </p>
  </details>
  <hr>
    <details>
      <summary>4. Buah-buahan</summary>
    <p>Sumber makanan sehat selanjutnya adalah buah-buahan. Jenis makanan yang satu ini sangat penting untuk menjaga kesehatan tubuh. Beberapa jenis buah-buahan yang baik untuk dikonsumsi antara lain: Jeruk, Apel, Manggis, Sirsak, Anggur, Kelengkeng, Alpukat. Sama seperti sayur-sayuran, kandungan gizi yang terdapat pada buah adalah mineral dan serat. 
      Selain itu, buah-buahan berfungsi bagi sebagai antidioksidan karena pada kulit buah mengandung pigmen warna klorofil seperti kiwi dan jambu. Semakin gelap warnanya semakin besar perlindungan antidioksidannya pada tubuh.</p>
  </details>
  <hr>
    <details>
      <summary>5. Susu</summary>
    <p>Susu berasal dari sumber protein hewani, fungsinya sangat baik bagi pertumbuhan tulang dan meningkatkan energi. Selain susu dari protein hewani, juga ada susu dari protein nabati yakni susu kedelai. Jika anak mama memiliki kelebihan berat badan, ia dapat mengonsumsi susu rendah lemak. Ada beberapa jenis susu yang dapat dikonsumi, berikut diantaranya: 
      <ol>
        <li>Susu almond cocok bagi seseorang yang alergi pada laktosa. Laktosa berlebih dapat menyebabkan produksi gas dan diare. </li>    
        <li>Susu kedelai mengandung kandungan lemak jenuh yang rendah. Maka dari itu, susu kedelai dapat membantu program diet dan cocok dijadikan susu pengganti susu sapi. </li>
        <li>Susu kambing mengandung 170 kalori, 10 gram lemak dan 27 mg kolesterol. Susu kambing memiliki anti alergi yang baik bagi tubuh.</li>
      </ol>
    </p>
  </details>
